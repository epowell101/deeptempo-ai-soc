// Custom integrations loaded dynamically
let customIntegrations: IntegrationMetadata[] = []

// Combined integrations (built-in + custom)
let allIntegrations: IntegrationMetadata[] = [...INTEGRATIONS]

/**
 * Load custom integrations from the backend
 */
export async function loadCustomIntegrations(): Promise<void> {
  try {
    const response = await fetch('http://localhost:6987/api/custom-integrations/list')
    const data = await response.json()
    
    if (data.success && data.integrations) {
      customIntegrations = data.integrations.map((metadata: any) => ({
        ...metadata,
        is_custom: true,
      }))
      
      // Merge with built-in integrations
      allIntegrations = [...INTEGRATIONS, ...customIntegrations]
      
      console.log(`Loaded ${customIntegrations.length} custom integrations`)
    }
  } catch (error) {
    console.error('Error loading custom integrations:', error)
    customIntegrations = []
    allIntegrations = [...INTEGRATIONS]
  }
}

/**
 * Get all integrations (built-in + custom)
 */
export function getAllIntegrations(): IntegrationMetadata[] {
  return allIntegrations
}

/**
 * Get custom integrations only
 */
export function getCustomIntegrations(): IntegrationMetadata[] {
  return customIntegrations
}

export function getIntegrationById(id: string): IntegrationMetadata | undefined {
  return allIntegrations.find((integration) => integration.id === id)
}

export function getIntegrationsByCategory(category: string): IntegrationMetadata[] {
  return allIntegrations.filter((integration) => integration.category === category)
}

